def get_field_defs_for_type(packet_type: str) -> List[Dict[str, Any]]:
    """Get field definitions for a packet type, handling aliases."""
    if packet_type.startswith('BTN_'):
        return FIELD_DEFS['BTN']
    elif packet_type.startswith('PAIR_B'):
        return FIELD_DEFS['PAIR']
    elif packet_type.startswith('BRIDGE_'):
        return FIELD_DEFS['UNKNOWN']
    return FIELD_DEFS.get(packet_type, FIELD_DEFS['UNKNOWN'])


def get_field_color(format_name: str) -> str:
    """Get field color for a given format."""
    return FIELD_FORMATS.get(format_name, {}).get('color', '#9E9E9E')


def parse_device_id(bytes_list: List[str], offset: int, endian: str) -> str:
    """Parse a device ID from bytes."""
    if len(bytes_list) < offset + 4:
        return ''
    b = bytes_list[offset:offset + 4]
    if endian == 'little':
        return f"{b[3]}{b[2]}{b[1]}{b[0]}".upper()
    return f"{b[0]}{b[1]}{b[2]}{b[3]}".upper()


def parse_level_byte(byte_str: str) -> str:
    """Parse a level byte (0x00-0xFE = 0-100%)."""
    value = int(byte_str, 16)
    if value == 0:
        return '0%'
    level = round((value * 100) / 254)
    return f"{level}%"


def parse_level_16bit(bytes_list: List[str]) -> str:
    """Parse a 16-bit level (0x0000-0xFEFF = 0-100%)."""
    if len(bytes_list) < 2:
        return ''
    value = int(bytes_list[0] + bytes_list[1], 16)
    if value == 0:
        return '0%'
    level = round((value * 100) / 65279)
    return f"{level}%"


